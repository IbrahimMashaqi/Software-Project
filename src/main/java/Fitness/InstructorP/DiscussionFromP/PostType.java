package Fitness.InstructorP.DiscussionFromP;

/**
 * Enum representing the type of post in a discussion.
 * <p>
 * The two types of posts are:
 * <ul>
 *     <li><b>newPost</b>: Represents a new post in the discussion.</li>
 *     <li><b>answeredPost</b>: Represents a post that has been answered or responded to.</li>
 * </ul>
 */
public enum PostType
{
    newPost,    // Represents a new post in the discussion.
    answeredPost,    // Represents a post that has been answered.
}
