package Fitness.InstructorP.ProgramPackage;

/**
 * Enum representing the possible statuses of a fitness program.
 * The program can either be active or deactivated.
 * <p>
 * This enum helps in tracking the current state of a program.
 * </p>
 * <p>
 * @author Omar Abumazen
 * </p>
 */
public enum ProgramStatus
{
    /**
     * Represents an active program that is currently running and available for clients.
     */
    Active,

    /**
     * Represents a deactivated program that is no longer available for clients.
     */
    DeaActive;
}
