package Fitness.InstructorP.ProgramPackage;

/**
 * Enum representing the completion status of a program.
 * <p>
 * The two possible statuses are:
 * <ul>
 *     <li><b>isComplete</b>: Indicates that the program is complete.</li>
 *     <li><b>isNotComplete</b>: Indicates that the program is not complete.</li>
 * </ul>
 */
public enum isComplete
{
    isComplete,    // Indicates that the program is complete.
    isNotComplete;    // Indicates that the program is not complete.
}
